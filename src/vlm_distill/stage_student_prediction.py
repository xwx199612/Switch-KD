class StudentBackend(Protocol):
    def answer(self, sample: VlmSample) -> dict:
        ...


class MockStudent:
    def answer(self, sample: VlmSample) -> dict:
        seed = f"{sample.id}:{sample.query}:{sample.answer or ''}"
        digest = hashlib.sha256(seed.encode("utf-8")).hexdigest()
        confidence = 0.55 + (int(digest[:4], 16) / 0xFFFF) * 0.4

        if sample.answer:
            student_answer = sample.answer
        elif sample.task == "parsing":
            student_answer = json.dumps(
                {
                    "elements": [
                        {"text": "mock icon", "bbox_norm": [0, 0, 100, 100], "focused": False},
                        {"text": "mock settings", "bbox_norm": [100, 0, 200, 100], "focused": True},
                    ],
                    "coordinate_system": COORDINATE_SYSTEM_NORMALIZED_0_1000,
                },
                ensure_ascii=False,
            )
        else:
            student_answer = f"mock answer for {sample.task}"

        return {
            "student_answer": student_answer,
            "student_confidence": round(confidence, 4),
            "student_rationale": "Mock student backend used for pipeline validation.",
        }


class HuggingFaceStudent:
    def __init__(self, config: PipelineConfig):
        self.config = config
        self.engine = BBoxGroundingInferenceEngine.from_pipeline_config(config)
        self.model, self.processor = self.engine.model, self.engine.processor

    def answer(self, sample: VlmSample) -> dict:
        prompt = _format_teacher_prompt(self.config, sample)
        image_path = self.config.data.image_root / sample.image
        image = _load_teacher_image(image_path, self.config.training.image_resize)

        answer = self.engine.generate_raw(image, prompt, self.config.teacher.max_new_tokens)
        result = {
            "student_answer": answer.strip(),
            "student_confidence": 1.0,
            "student_rationale": "Generated by Hugging Face student backend.",
        }
        if self.engine.debug_inference_parity:
            result["inference_debug"] = dict(self.engine.last_debug)
        return result

    def tokenize_student_answer(self, answer: str) -> list[int]:
        tokenizer = getattr(self.processor, "tokenizer", None)
        if tokenizer is None:
            encoded = self.processor(text=[answer], return_tensors=None)
            input_ids = encoded["input_ids"][0]
        else:
            input_ids = tokenizer(answer, add_special_tokens=False)["input_ids"]
        return [int(token_id) for token_id in input_ids]


def build_student_backend(config: PipelineConfig) -> StudentBackend:
    if config.student.model_name.startswith("mock-"):
        return MockStudent()
    return HuggingFaceStudent(config)


@dataclass(frozen=True)
class PredictionModelSelection:
    source: str
    model_path: str
    should_load_adapter: bool
    deployment_path: Path | None = None


def _resolve_prediction_model_selection(config: PipelineConfig) -> PredictionModelSelection:
    inference_model_path = (config.student.inference_model_path or "").strip()
    merged_model_path = config.student.merged_model_path

    if merged_model_path is not None and (merged_model_path / "deployment_config.json").exists():
        return PredictionModelSelection(
            source="4bit_base_bf16_adapter", model_path=str(merged_model_path),
            should_load_adapter=False, deployment_path=merged_model_path,
        )

    if inference_model_path:
        candidate = Path(inference_model_path)
        if (candidate / "deployment_config.json").exists():
            return PredictionModelSelection(
                source="4bit_base_bf16_adapter", model_path=str(candidate),
                should_load_adapter=False, deployment_path=candidate,
            )
        return PredictionModelSelection(
            source="inference_model_path",
            model_path=resolve_model_path(inference_model_path),
            should_load_adapter=_should_load_prediction_adapter(config),
        )

    if merged_model_path is not None and merged_model_path.exists():
        return PredictionModelSelection(
            source="merged_model_path",
            model_path=resolve_model_path(str(merged_model_path)),
            should_load_adapter=False,
        )

    return PredictionModelSelection(
        source="base_model_plus_adapter",
        model_path=resolve_model_path(config.student.model_name),
        should_load_adapter=_should_load_prediction_adapter(config),
    )


def _should_load_prediction_adapter(config: PipelineConfig) -> bool:
    return config.student.load_adapter or config.student.merge_adapter


def _resolve_prediction_adapter_path(config: PipelineConfig) -> Path:
    return config.student.inference_adapter_path or config.student.adapter_dir


def _validate_prediction_adapter_path(adapter_path: Path) -> None:
    if not adapter_path.exists():
        raise FileNotFoundError(
            "Prediction adapter path does not exist: "
            f"{adapter_path}. Set student.inference_adapter_path to a saved adapter directory "
            "or disable adapter loading for prediction."
        )

    adapter_config = adapter_path / "adapter_config.json"
    if not adapter_config.exists():
        raise FileNotFoundError(
            "Prediction adapter path is missing adapter_config.json: "
            f"{adapter_config}. Ensure this path points to a PEFT adapter directory."
        )


def create_student_predictions(config: PipelineConfig, samples: list[VlmSample]) -> Path:
    output_path = resolve_prediction_path(config.data)
    completed_ids = _load_completed_ids(output_path)
    total = len(samples)
    pending_samples = [sample for sample in samples if sample.id not in completed_ids]

    output_path.parent.mkdir(parents=True, exist_ok=True)
    print(
        f"Predicting samples: total={total}, completed={len(completed_ids)}, "
        f"pending={len(pending_samples)}, output={output_path}"
    )

    if not pending_samples:
        refresh_parsing_sidecar_reports(output_root=output_path.parent, role="student")
        print("No pending samples. Existing prediction output is already complete for this manifest.")
        return output_path

    student = build_student_backend(config)
    completed_now = 0
    with output_path.open("a", encoding="utf-8") as handle:
        for index, sample in enumerate(pending_samples, start=1):
            started = time.perf_counter()
            row, raw_model_output = _predict_sample(student, sample)
            if sample.task == "parsing":
                parsed = write_parsing_sidecar(
                    row=_base_prediction_row(sample),
                    output_root=output_path.parent,
                    role="student",
                    raw_model_output=raw_model_output,
                )
                if row is not None:
                    handle.write(json.dumps(row, ensure_ascii=False) + "\n")
                    handle.flush()
                else:
                    print(
                        f"Warning: unusable student parsing output for id={sample.id}: "
                        f"{parsed.get('parse_error')}"
                    )
            else:
                handle.write(json.dumps(row, ensure_ascii=False) + "\n")
                handle.flush()
            completed_now += 1
            elapsed = time.perf_counter() - started
            total_done = len(completed_ids) + completed_now
            print(
                f"[predict] wrote {total_done}/{total} "
                f"id={sample.id} elapsed={elapsed:.2f}s"
            )

    refresh_parsing_sidecar_reports(output_root=output_path.parent, role="student")
    return output_path


def _base_prediction_row(sample: VlmSample) -> dict:
    return {
        "id": sample.id,
        "image": sample.image,
        "task": sample.task,
        "query": sample.query,
    }


def _predict_sample(student: StudentBackend, sample: VlmSample) -> tuple[dict | None, str]:
    prediction = student.answer(sample)
    raw_model_output = str(prediction["student_answer"]).strip()
    if sample.task != "parsing":
        return {
            **_base_prediction_row(sample),
            "student_answer": raw_model_output,
        }, raw_model_output
    parsed = parse_parsing_answer(raw_model_output)
    if not parsed.get("usable"):
        return None, raw_model_output
    result = {
        **_base_prediction_row(sample),
        "elements": parsed["elements"],
        "coordinate_system": COORDINATE_SYSTEM_NORMALIZED_0_1000,
    }
    if prediction.get("inference_debug"):
        result["inference_debug"] = prediction["inference_debug"]
    return result, raw_model_output


def _load_completed_ids(path: Path) -> set[str]:
    if not path.exists():
        return set()

    completed_ids: set[str] = set()
    for row in read_jsonl(path):
        sample_id = row.get("id")
        if sample_id is not None:
            completed_ids.add(str(sample_id))
    return completed_ids
